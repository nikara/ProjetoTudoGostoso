package TudoGostoso;

public class Receita {
    int IdReceita;
    String titulo;
    String descricao;
    String imagem;

    public Receita(int IdReceita, String titulo, String descricao, String imagem){
        this.IdReceita = IdReceita;
        this.titulo = titulo;
        this.descricao = descricao;
        this.imagem = imagem;
    }

}
